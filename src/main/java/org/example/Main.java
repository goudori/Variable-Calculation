package org.example;

public class Main {

  /**
   * 課題 →文字列結合と数値計算結果を結合して、「自分の名前+ "さんの年齢は、" + 自分の年齢 +"歳です。"」とターミナルに出力する。
   *
   * @param args
   */
  public static void main(String[] args) {
    String name = "あっきー";

    int age = 15 + 16;

    System.out.println(name + "さんの年齢は、" + age + "歳です。");

  }
}
